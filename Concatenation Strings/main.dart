void main() {
  String a = 'We';
  String b = 'Are';
  String c = 'Learning';
  String d = 'Dart';
  print(a + b + c + d);

  // String Listing
  String e = 'We' 'Are' 'Learning' 'Dart';
  print(e);

  // Interpulation Symble($)
  print('$a$b$c$d');

  // Array join
  List<String> f = ['We', 'Are', 'Learning', 'Dart'];
  print(f.join(' '));
}
